    private void cargarTabla() {
        // Inicializa el modelo de la tabla y establece las columnas
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String[] titulos = {"Nombre", "Ganancia"};
        modeloTabla.setColumnIdentifiers(titulos);
        modeloTabla.setRowCount(0);

        // Itera las productos
        for (Categoria cat : categorias) {
            Object[] obj = {
                cat.getNombre(),
                mostrarEnPorcentaje(cat.getGanancia())
            };
            modeloTabla.addRow(obj);
        }

        jtCategorias.setModel(modeloTabla);

        // Centra el texto en todas las celdas
        DefaultTableCellRenderer centrado = new DefaultTableCellRenderer();
        centrado.setHorizontalAlignment(SwingConstants.CENTER);

        // Aplica el render centrado a cada columna
        for (int i = 0; i < jtCategorias.getColumnCount(); i++) {
            jtCategorias.getColumnModel().getColumn(i).setCellRenderer(centrado);
        }
    }
