    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (jtCategorias.getRowCount() > 0) {
            int filaSelect = jtCategorias.getSelectedRow();
            //Asegurar selección
            if (filaSelect != -1) {
                Categoria categoriaSelect = categorias.get(filaSelect);
                boolean conf = confirmar("¿Desea eliminar esta categoría?");
                if (conf) {
                    control.getControladoraCategoria().eliminarCategoria(categoriaSelect.getIdCategoria());
                    mostrarMensaje("Categoría eliminada", "informacion");
                    cargarTabla();
                }
            } else {
                mostrarMensaje("Seleccione la fila a eliminar", "advertencia");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

