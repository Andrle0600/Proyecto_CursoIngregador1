    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean conf = confirmar("¿Desea guardar los cambios?");
        if (conf) {
            //Obtención de datos
            String prodSelect = txtProducto.getText();
            Producto producto= control.getControladoraProducto().leerPorNombre(prodSelect);
            Object valor = spnCantidad.getValue();
            int cantidad;
            try {
                // Verificar si el valor es un entero
                if (valor instanceof Integer) {
                    cantidad = (int) valor; // Ya es un número entero, así que puedes asignarlo
                } else {
                    throw new NumberFormatException("Valor no numérico");
                }
            } catch (NumberFormatException e) {
                mostrarMensaje("No se permite el valor no numérico", "error");
                return;
            }
            double nSubtotal=cantidad*producto.getPrecioCompra();
            //actualizar total
            double total=ped.getTotal();
            double subtotal=detalle.getSubtotal();
            double nTotal=total-subtotal+nSubtotal;
            //guardar datos
            ped.setTotal(nTotal);
            control.getControladoraPedido().actualizarPedido(ped);
            detalle.setCantidad(cantidad);
            detalle.setSubtotal(nSubtotal);
            control.getControladoraDetallePedido().actualizarDetallePedido(detalle);
            //Confirmación del guardado
            mostrarMensaje("Datos del pedido actualizados exitosamente", "informacion");
            VerDetallesPedido listar = new VerDetallesPedido(ped);
            listar.setVisible(true);
            listar.setLocationRelativeTo(null);
            this.dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void cargarDatos(int id) {
        //obtener datos
        String producto = detalle.getProducto().getNombre();
        int cant = detalle.getCantidad();
        //cargar datos
        txtProducto.setText(producto);
        spnCantidad.setValue(cant);
    }

    private ImageIcon loadImage(String imageName) {
        String imagePath = System.getProperty("user.dir") + "\\src\\main\\java\\Imagenes\\" + imageName;
        return new ImageIcon(imagePath);
    }

    public boolean confirmar(String mensaje) {
        int respuesta = JOptionPane.showConfirmDialog(
                null,
                mensaje,
                "Confirmación",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        return (respuesta == JOptionPane.YES_OPTION);
    }

    private static void mostrarMensaje(String mensaje, String tipo) {
        int tipoMensaje;

        tipoMensaje = switch (tipo.toLowerCase()) {
            case "error" ->
                JOptionPane.ERROR_MESSAGE;
            case "informacion" ->
                JOptionPane.INFORMATION_MESSAGE;
            case "advertencia" ->
                JOptionPane.WARNING_MESSAGE;
            case "pregunta" ->
                JOptionPane.QUESTION_MESSAGE;
            default ->
                JOptionPane.PLAIN_MESSAGE;
        };

        JOptionPane.showMessageDialog(null, mensaje, "Mensaje", tipoMensaje);
    }

